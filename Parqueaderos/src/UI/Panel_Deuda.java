    private void PagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PagoActionPerformed
        Deuda deuda = new Deuda();
        String usuario=tUsuario.getText();                              
        for(Externo e: Principal.BDExterno){
            if(e.getNombre().equals(usuario)){
                externoactual=e;
                System.out.println(externoactual.getDeuda());
                deuda.setSize(300,400);
                deuda.setLocation(0,0);
                panel.removeAll();
                panel.add(deuda);
                panel.revalidate();
                panel.repaint();  
                deuda.obtenerDeuda(e.getDeuda());
            }else{
            }
        }
        if(externoactual==null){
            JFrame parentFrame = new JFrame();
            JOptionPane.showMessageDialog(parentFrame, "Usuario incorrecto");
            tUsuario.setText("");
        }
    }//GEN-LAST:event_PagoActionPerformed

